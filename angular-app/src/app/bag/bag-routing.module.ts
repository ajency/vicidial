import { NgModule } from '@angular/core';
import { Routes, RouterModule } from '@angular/router';

import { ShippingDetailsComponent } from './shipping-details/shipping-details.component';
import { ShippingSummaryComponent } from './shipping-summary/shipping-summary.component';
import { BagViewComponent } from './bag-view/bag-view.component';

const routes: Routes = [
	{ path: '', component: BagViewComponent },
	{ path: 'shipping-address', component: ShippingDetailsComponent },
	{ path: 'shipping-summary', component: ShippingSummaryComponent },
	{ path: '**', component : BagViewComponent}
];

@NgModule({
  imports: [RouterModule.forChild(routes)],
  exports: [RouterModule]
})
export class BagRoutingModule { }
